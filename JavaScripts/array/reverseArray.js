/*..................Reversing an Array..........................*/
//The reverse() method reverses the elements in an array.

// You can use it to sort an array in descending order:

// Example
console.log("------------------Reversing an Array----------------");
const fruits14 = ["Banana", "Orange", "Apple", "Mango"];
fruits14.sort();
fruits14.reverse();
console.log(fruits14);

